#pragma once

#include "SpriteCollection.hpp"
#include "Collider.hpp"

namespace gw {

/// A single room node in the GameMap
class GameRoom : public SpriteCollection
{

    friend class Game; // Game needs to access colliders

public:

////////////////////////////////////////////////////////////////////////////////////////////////////
// Constructors
////////////////////////////////////////////////////////////////////////////////////////////////////

    GameRoom(std::string name = "No name");

    ~GameRoom();

////////////////////////////////////////////////////////////////////////////////////////////////////
// Mutators
////////////////////////////////////////////////////////////////////////////////////////////////////

    void addCollider(Collider& collider);

////////////////////////////////////////////////////////////////////////////////////////////////////
// Public Members
////////////////////////////////////////////////////////////////////////////////////////////////////

    std::string roomName;

    // Connections to other GameRooms //
    GameRoom* top;
    GameRoom* bottom;
    GameRoom* left;
    GameRoom* right;

////////////////////////////////////////////////////////////////////////////////////////////////////
// Private Members
////////////////////////////////////////////////////////////////////////////////////////////////////

private:

    std::vector<Collider*> colliders;   // Collision detection and resolution

};

} // namespace gw