#pragma once

namespace gw 
{

/// Container for two unsigned ints.
/// Operations with negative results default members to zero.
class Vector2u
{
public:

///////////////////////////////////////////////////////////////////////////////////////////////////
// Constructors 
///////////////////////////////////////////////////////////////////////////////////////////////////

	Vector2u(int x = 0, int y = 0);
	
///////////////////////////////////////////////////////////////////////////////////////////////////
// Operators 
///////////////////////////////////////////////////////////////////////////////////////////////////

	Vector2u operator+(int n);
	Vector2u operator-(int n);
	Vector2u operator*(int n);
	Vector2u operator/(int n);
	Vector2u operator%(int n);

	Vector2u operator+(Vector2u v);
	Vector2u operator-(Vector2u v);
	Vector2u operator*(Vector2u v);
	Vector2u operator/(Vector2u v);
	Vector2u operator%(Vector2u v);

///////////////////////////////////////////////////////////////////////////////////////////////////
// Public Members 
///////////////////////////////////////////////////////////////////////////////////////////////////

	unsigned int x, y;
};

}